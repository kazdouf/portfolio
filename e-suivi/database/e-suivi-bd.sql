CREATE DEFINER=`root`@`localhost` PROCEDURE `cumulAbsences` (IN `matStg` VARCHAR(45))  BEGIN
	SELECT SEC_TO_TIME(SUM(TIME_TO_SEC(s.duree))) AS `cumul`
    FROM seance s JOIN absence a ON (s.codeSeance=a.codeSeance)
    WHERE a.matriculeEtd = matStg;
END$$

CREATE DEFINER=`root`@`localhost` PROCEDURE `GetAbsences` (IN `grp` VARCHAR(45))  BEGIN
	SELECT a.codeSeance, m.nomModule, a.matriculeEtd,s.jour, s.heurDebut, s.heurFin, a.justifiee , concat(st.nom , ' ', st.prenom) as `fullName`
 	FROM absence a
	JOIN stagiaire st ON (st.matriculeEtudiant = a.matriculeEtd)
    JOIN seance s ON (a.codeSeance = s.codeSeance)
    JOIN affectation af ON (s.codeAffectation = af.codeAffectation)
    JOIN module m ON (af.codeModule = m.codeModule)
    JOIN groupe g ON (g.codeGroupe = af.codeGroupe)
    WHERE st.codeGroupe = grp
    AND s.jour between DATE_SUB(CURDATE(), INTERVAL 5 DAY) AND CURDATE();
END$$

